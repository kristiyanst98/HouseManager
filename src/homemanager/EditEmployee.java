    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        int selectedIndex = jComboBox1.getSelectedIndex();
        Employee e = employees.get(selectedIndex);
        employeeName.setText(e.getName());
        employeeSN.setText(e.getStaffNr() + "");
        employeeSalary.setText(e.getSalary() + "");
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (employeeName.getText().isEmpty() || employeeSalary.getText().isEmpty() || employeeSN.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all fields");
        } else {
            int selectedIndex = jComboBox1.getSelectedIndex();
            employees.get(selectedIndex).setName(employeeName.getText());
            employees.get(selectedIndex).setStaffNr(Integer.parseInt(employeeSN.getText()));
            employees.get(selectedIndex).setSalary(Double.parseDouble(employeeSalary.getText()));
            saveEmployeesToFile();

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedIndex = jComboBox1.getSelectedIndex();
        employees.remove(selectedIndex);
        saveEmployeesToFile();
    }//GEN-LAST:event_jButton2ActionPerformed

